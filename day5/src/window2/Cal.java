package window2;

public class Cal {
	
	// public/private 리턴타입 메소드명(입력값){}

	public void add(int a, int b) {
		System.out.println("+result : " + (a + b));
	}

	public void minus(int a, int b) {
		System.out.println("-result : " + (a - b));
	}

	public void multiple(int a, int b) {
		System.out.println("*result : " + (a * b));
	}

	public void divide(int a, int b) {
		System.out.println("/result : " + (a / b));
	}

}
