package window2;

public class Cal2 {
	
	// public/private 리턴타입 메소드명(입력값){}

	public int add(int a, int b) {
		int result = a+b;
		return result;
	}

	public int minus(int a, int b) {
		int result = a-b;
		return result;
	}

	public int multiple(int a, int b) {
		int result = a*b;
		return result;
	}

	public int divide(int a, int b) {
		int result = a/b;
		return result;
	}
	
	public int myCal(int a, int b) {
		int result = a + b + 100;
		return result;
	}

}
